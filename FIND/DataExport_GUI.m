        otherwise %ERROR
            error(['Unknown file type -> Check the parameters: ']);
    end


    %-% rest of GUI below...


catch
    % if error occurs here, the window is closed, the error is rethrown
    % and then catched by the main window
    close(findobj('tag', 'DataExportGUI'));
    rethrow(lasterror);
end


