        'Tag','fileTypeSelectionGUI_radioButton1');

    radioButton2 = uicontrol('Style','Radio',...
        'String','Export GDF',...
        'Units','characters',...
        'pos',[20 .2 15 BUTTON_HEIGHT],...
        'parent',radioGroup,...
        'HandleVisibility','off',...
        'Tag','fileTypeSelectionGUI_radioButton2');

    radioButton3 = uicontrol('Style','Radio',...
